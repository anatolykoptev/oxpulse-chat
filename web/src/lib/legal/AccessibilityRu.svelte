<h2>Наша цель</h2>
<p>Мы стремимся сделать ОксПалс (OxPulse) доступным для всех пользователей, независимо от
их возможностей и используемых технологий.</p>

<h2>Стандарт</h2>
<p>Мы ориентируемся на стандарт WCAG 2.1 уровня AA.</p>

<h2>Что реализовано</h2>
<ul>
  <li>Навигация с клавиатуры по всем элементам управления</li>
  <li>ARIA-метки для интерактивных элементов</li>
  <li>Достаточный контраст текста</li>
  <li>Адаптивная вёрстка для различных устройств и размеров экрана</li>
</ul>

<h2>Над чем работаем</h2>
<ul>
  <li>Субтитры в реальном времени</li>
  <li>Улучшенная поддержка screen reader</li>
</ul>

