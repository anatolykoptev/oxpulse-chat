<h2>Our Commitment</h2>
<p>We are committed to making OxPulse accessible to everyone, regardless of ability
or technology.</p>

<h2>Standard</h2>
<p>We target WCAG 2.1 Level AA compliance.</p>

<h2>Current Features</h2>
<ul>
  <li>Full keyboard navigation for all controls</li>
  <li>ARIA labels on interactive elements</li>
  <li>Sufficient color contrast ratios</li>
  <li>Responsive design for all devices and screen sizes</li>
</ul>

<h2>In Progress</h2>
<ul>
  <li>Real-time captions during calls</li>
  <li>Screen reader optimization</li>
</ul>

