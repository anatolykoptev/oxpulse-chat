<h2>3. Цель обработки</h2>
<p>Обеспечение работы видеозвонков между пользователями.</p>

<h2>4. Срок хранения</h2>
<p>Данные хранятся только на время звонка и автоматически удаляются
при его завершении. Постоянное хранение персональных данных не осуществляется.</p>

<h2>5. Cookies и локальное хранилище</h2>
<p>Сервис использует только техническое хранилище (localStorage) для сохранения
выбранного языка интерфейса. Мы не используем cookies для отслеживания, аналитики
или рекламы. Трекеры отсутствуют.</p>

<h2>6. Шифрование</h2>
<p>Все медиа-данные шифруются по протоколу DTLS-SRTP между устройствами участников.</p>

<h2>7. Медиа-трафик</h2>
<p>Медиа-трафик передаётся через TURN-сервер, расположенный в России.</p>

